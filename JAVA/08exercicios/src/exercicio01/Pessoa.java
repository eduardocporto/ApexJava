package exercicio01;

public class Pessoa {
	
//ATRIBUTOS
	private String nome, cidade;
	private int idade;
	
//GET E SET	NOME
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}

//GET E SET	CIDADE	
	public String getCidade() {
		return cidade;
	}
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
	
//GET E SET	IDADE
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	

	

}
